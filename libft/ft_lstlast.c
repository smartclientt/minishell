#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*pl;

	pl = lst;
	while (pl)
	{
		if (pl->next == NULL)
			return (pl);
		pl = pl->next;
	}
	return (NULL);
}
